                        <p>Om het logboek te openen klik je in de agenda op de knop “Logboek beheren”.</p>

                        <p>Wanneer op het logboek krijg je het volgende scherm te zien. Binnen het logboek kan je voor
                            elk van de events een mededeling/log bijhouden. In het logboek krijg je je eigen logs te
                            zien, die van je bezoekers en die van je mantelzorger.</p>
                        <p class="d-flex justify-content-center"><img
                                src="/img/user-guide/Logboek/logboek-overzicht.png" class="w-100 shadow"></p>
                        <h4>Logs toevoegen</h4>
                        <p>Om een log in te voeren klik je op de knop “Nieuwe log”. In het dialoogvenster dat dan
                            tevoorschijn komt geef je de nodige gegevens mee. In het dialoogvenster dat dan tevoorschijn
                            komt geef je mee welke titel, inhoud, datum en bezoeker. Ben je daarmee klaar dan klik je op
                            de knop “Log opslaan”.</p>
                        <p>Ook vanuit de agenda kan je een log invoegen. Dit doe je door op de knop “Nieuwe log” te
                            klikken. Je krijgt vervolgens hetzelfde dialoogvenster te zien.</p>
                        <p class="d-flex justify-content-center"><img
                                src="/img/user-guide/Logboek/logboek-log-toevoegen.png" class="w-50 shadow"></p>
                        <h4>Logs inkijken / toevoegen / verwijderen</h4>
                        <p>Wanneer je bestaande logs wilt inkijken klik je op de knop "<i
                                class="fas fa-info-circle"></i>". Wil je een log bewerken klik je op volgende knop "<i
                                class="fas fa-edit"></i>". Kies je er voor om een log te verwijderen dan klik je op de
                            knop "<i class="fas fa-trash-alt"></i>". </p>
                    </div>
                    <div class="tab-pane fade mt-3" id="nav-med" role="tabpanel" aria-labelledby="nav-med-tab">
                        <p>Om je medisch dossier te bekijken klik je in de agenda op de knop “Medicatie beheren”. Je
                            krijgt volgend scherm te zien.</p>
                        <p class="d-flex justify-content-center"><img
                                src="/img/user-guide/Dossier/dossier-overzicht.png" class="w-100 shadow"></p>
                        <p>Op je dossier krijg je al de medicatie te zien die zelf voorgeschreven zijn en al de
                            medicatie voorgeschreven door de dokter. Per medicatie lijn krijg je de volgende gegevens te
                            zien: naam, dosis, reden, startdatum, einddatum, voorschrift. In de agenda kan je deze
                            medicatie inplannen in de agenda (zie events).</p>
                        <h4>Medicatie toevoegen</h4>
                        <p>Wil zelf medicatie toevoegen aan je dossier dan klik je op de knop “Nieuwe medicatie
                            toevoegen”. In het dialoogvenster dat dan tevoorschijn komt geef je mee welke medicatie je
                            wilt toevoegen, de dosis, reden en start- en einddatum. Al deze velden zijn verplicht in te
                            vullen. Vervolgens kan je deze toevoegen door op de knop “Voorschrift opslaan” te
                            klikken.</p>
                        <p class="d-flex justify-content-center"><img
                                src="/img/user-guide/Dossier/dossier-medicatie-toevoegen.png" class="w-50 shadow"></p>
                        <h4>Medicatie bewerken / verwijderen</h4>
                        <p>Wanneer je bestaande medicatie wilt bewerken klik je op volgende knop "<i
                                class="fas fa-edit"></i>". Wil je bestaande medicatie verwijderen dan klik je op de knop
                            “<i class="fas fa-trash-alt"></i>”. Je kan enkel medicatie wijzigen of verwijderen die je
                            zelf hebt
                            toegevoegd. Medicatie op doktersvoorschrift kan niet worden aangepast.</p>
                    </div>
                    <div class="tab-pane fade mt-3" id="nav-event" role="tabpanel" aria-labelledby="nav-event-tab">
                        <p>In je agenda krijg je een overzicht van alle geplande events te zien per week. </p>
                        <p class="d-flex justify-content-center"><img
                                src="/img/user-guide/Agenda/agenda-overzicht.png" class="w-100 shadow"></p>
                        <h4>Events inplannen</h4>
                        <p>Wil je daar een event aan toevoegen dan klik je op de knop “Nieuw event”. In het
                            dialoogvenster dat volgt maak je eerst de keuze voor het type event. Daar kan je kiezen
                            voor: taak, afspraak of medicatie. Op basis van de keuze die je hier maakt krijg je een
                            ander formulier te zien.</p>
                        <p class="d-flex justify-content-center"><img
                                src="/img/user-guide/Agenda/agenda-event-new.png" class="w-50 shadow"></p>
                        <h5>Taak</h5>
                        <p>Het eventtype taak is het basis eventtype dat je kan gebruiken voor simpele taken. Voor een
                            taak in te plannen moet je de titel, beschrijving en tijdstip van de taak meegeven. Je kan
                            er ook voor kiezen om deze te herhalen. Ben je tevreden met de instellingen dan kan je de
                            taak in de agenda zetten door op “opslaan” te klikken.</p>
                        <p class="d-flex justify-content-center"><img
                                src="/img/user-guide/Agenda/agenda-event-task.png" class="w-50 shadow"></p>
                        <h5>Afspraak</h5>
                        <p>Het eventtype afspraak kan je gebruiken voor events waarbij je een ontmoeting hebt met een
                            andere persoon. Naast de standaard velden die je ook bij taken terugvindt kan je hier ook
                            een locatie en een contactpersoon meegeven.</p>
                        <p class="d-flex justify-content-center"><img
                                src="/img/user-guide/Agenda/agenda-event-appointment.png" class="w-50 shadow"></p>
                        <h5>Medicatie</h5>
                        <p>Het eventtype medicatie is voorzien om de medicatie van het medisch dossier in te plannen in
                            de agenda. Voor dit eventtype kies je welke medicatie je wilt inplannen samen met het
                            tijdstip waarop je de medicatie wilt innemen.</p>
                        <p class="d-flex justify-content-center"><img
                                src="/img/user-guide/Agenda/agenda-event-med.png" class="w-50 shadow"></p>
                        <h4>Een event inkijken / bewerken / verwijderen</h4>
                        <p>Je kan een event inkijken door op eventkader in de agenda te klikken. Wil je dit dan bewerken
                            dan kan je klikken op de knop “bewerken”. Om een event te verwijderen moet je eerst in de
                            bewerkingsweergave zitten, dan krijg je onderaan de knop “Verwijder” of “Verwijder alleen
                            deze dag” te zien. De knop “Verwijder” verwijdert het event waarin je nu aan het werken bent
                            samen met alle herhalingen van dat event. De knop “Verwijder alleen deze dag” verwijdert
                            alleen dit event.</p>
